package xyz.liangxin.utils.constant.text;

/**
 * <p> 字符串常量
 *
 * @author liangxin
 * @version V1.0
 * @since 创建时间: 2022/1/6 18:14
 */
public class CharSequenceConstant {
    public static final int INDEX_NOT_FOUND = -1;

    /**
     * 字符串常量：{@code "null"} <br>
     * 注意：{@code "null" != null}
     */
    public static final String STR_NULL = "null";

    /**
     * 字符串常量：空字符串 {@code ""}
     */
    public static final String STR_EMPTY = "";

    /**
     * 字符串常量：空格符 {@code " "}
     */
    public static final String STR_SPACE = " ";

    /**
     * 字符串常量：制表符 {@code "\t"}
     */
    public static final String STR_TAB = "\u0009";

    /**
     * 字符串常量：点 {@code "."}
     */
    public static final String STR_DOT = ".";

    /**
     * 字符串常量：双点 {@code ".."} <br>
     * 用途：作为指向上级文件夹的路径，如：{@code "../path"}
     */
    public static final String STR_DOUBLE_DOT = "..";

    /**
     * 字符串常量：斜杠 {@code "/"}
     */
    public static final String STR_SLASH = "/";
    /**
     * 字符串常量：双斜杠 {@code "//"}
     */
    public static final String STR_DOUBLE_SLASH = "//";

    /**
     * 字符串常量：反斜杠 {@code "\\"}
     */
    public static final String STR_BACKSLASH = "\\";

    /**
     * 字符串常量：回车符 {@code "\r"} <br>
     * 解释：该字符常用于表示 Linux 系统和 MacOS 系统下的文本换行
     */
    public static final String STR_CR = "\r";

    /**
     * 字符串常量：换行符 {@code "\n"}
     */
    public static final String STR_LF = "\n";

    /**
     * 字符串常量：Windows 换行 {@code "\r\n"} <br>
     * 解释：该字符串常用于表示 Windows 系统下的文本换行
     */
    public static final String STR_CRLF = "\r\n";

    /**
     * 字符串常量：下划线 {@code "_"}
     */
    public static final String STR_UNDERLINE = "_";

    /**
     * 字符串常量：减号（连接符） {@code "-"}
     */
    public static final String STR_DASHED = "-";

    /**
     * 字符串常量：逗号 {@code ","}
     */
    public static final String STR_COMMA = ",";

    /**
     * 字符串常量：花括号（左） <code>"{"</code>
     */
    public static final String STR_DELIM_START = "{";

    /**
     * 字符串常量：花括号（右） <code>"}"</code>
     */
    public static final String STR_DELIM_END = "}";

    /**
     * 字符串常量：中括号（左） {@code "["}
     */
    public static final String STR_BRACKET_START = "[";

    /**
     * 字符串常量：中括号（右） {@code "]"}
     */
    public static final String STR_BRACKET_END = "]";

    /**
     * 字符串常量：冒号 {@code ":"}
     */
    public static final String STR_COLON = ":";

    /**
     * 字符串常量：艾特 {@code "@"}
     */
    public static final String STR_AT = "@";
    /**
     * 字符串常量：星号/乘法符号  {@code "*"}
     */
    public static final String STR_MULTIPLY = "*";

    /**
     * 字符串常量：UNKNOWN {@code "UNKNOWN"}
     */
    public static final String STR_UNKNOWN = "unknown";

    /**
     * 字符串常量：与符号 {@code "&"}
     */
    public static final String STR_AMP = "&;";


    /**
     * 字符串常量：空 JSON {@code "{}"}
     */
    public static final String STR_EMPTY_JSON = "{}";


    /**
     * 字符串常量：加号 {@code "+"}
     */
    public static final String STR_ADD = "+";

    /**
     * 字符串常量：等于号 {@code "="}
     */
    public static final String STR_EQUAL = "=";

    /**
     * 字符串常量：问号 {@code "?"}
     */
    public static final String STR_QUESTION = "?";


    /**
     * 字符串常量：HTML 空格转义 {@code "&nbsp;" -> " "}
     */
    public static final String STR_HTML_NBSP = "&nbsp;";

    /**
     * 字符串常量：HTML And 符转义 {@code "&amp;" -> "&"}
     */
    public static final String STR_HTML_AMP = "&amp;";

    /**
     * 字符串常量：HTML 双引号转义 {@code "&quot;" -> "\""}
     */
    public static final String STR_HTML_QUOTE = "&quot;";

    /**
     * 字符串常量：HTML 单引号转义 {@code "&apos" -> "'"}
     */
    public static final String STR_HTML_APOS = "&apos";

    /**
     * 字符串常量：HTML 小于号转义 {@code "&lt;" -> "<"}
     */
    public static final String STR_HTML_LT = "&lt;";

    /**
     * 字符串常量：HTML 大于号转义 {@code "&gt;" -> ">"}
     */
    public static final String STR_HTML_GT = "&gt;";



}
